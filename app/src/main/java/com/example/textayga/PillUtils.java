package com.example.textayga;

public class PillUtils {

    // Метод для получения строкового представления количества таблеток с правильными склонениями
    public static String getPillCountString(String count) {
        try {
            // Извлекаем число из строки, удаляя все нецифровые символы
            int num = Integer.parseInt(count.replaceAll("[^0-9]", ""));

            // Проверка на склонение "таблетка/таблетки/таблеток" для чисел от 11 до 19
            if (num % 100 >= 11 && num % 100 <= 19) {
                return num + " таблеток"; // В русском языке для чисел 11-19 всегда используется "таблеток"
            }

            // Определение склонения в зависимости от последней цифры числа
            switch (num % 10) {
                case 1: return num + " таблетка"; // Для 1 используется "таблетка"
                case 2:
                case 3:
                case 4: return num + " таблетки"; // Для 2, 3, 4 — "таблетки"
                default: return num + " таблеток"; // Для остальных чисел используется "таблеток"
            }
        } catch (NumberFormatException e) {
            // Если не удалось преобразовать строку в число, возвращаем исходную строку с добавлением "таблеток"
            return count + " таблеток";
        }
    }
}
